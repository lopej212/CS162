#include "Square.hpp"
/*********************************************
 * Description:
 * This function will set the length and width
 * of the square object when the length is changed.
 *********************************************/
void Square::setLength( double l ) 
{
   length = l;
   width = l;

}
/**********************************************
 * Description:
 * This function will change the length and width
 * of the square object when the width is changed.
 **********************************************/
void Square::setWidth( double w)
{
   length = w;
   width = w;

}

